function readNestedValue(source, path) {
  return path.split('.').reduce((value, segment) => value?.[segment], source);
}

export function translate(language, key, params = {}) {
  const scopedTranslations = translations[language] ?? translations[DEFAULT_LANGUAGE];
  const template = readNestedValue(scopedTranslations, key) ?? readNestedValue(translations[DEFAULT_LANGUAGE], key);

  if (typeof template !== 'string') {
    return key;
  }

  return template.replace(/\{(\w+)\}/g, (_, token) => String(params[token] ?? ''));
}
